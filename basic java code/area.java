class area
{
    public static void main(String args[])
    {
        //area of circle
        int redius=5;
        double area_of_circle;
        

        area_of_circle=3.14*redius*redius;
        System.out.println("area of circle="+area_of_circle);
        
        //area of square

       int side=10;
       int area_of_square;
    
        area_of_square=side*side;
        System.out.println("area of square="+area_of_square);
        
        //area of triangle

        int height=10;
        int base=10;
       double area_of_triangle;

        area_of_triangle=height*base/2;
        System.out.println("area of triangle="+area_of_triangle);
           
           //area of rectangle

        int length=5;
        int width=5;
       double area_of_rectangle;
        

        area_of_rectangle=length*width;
        System.out.println("area of rectangle="+
        area_of_rectangle);

    }
}